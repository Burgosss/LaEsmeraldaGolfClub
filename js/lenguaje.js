// Función para cambiar idioma
function cambiarIdioma(idioma) {
  document.querySelectorAll("[data-i18n]").forEach(elemento => {
      const clave = elemento.getAttribute("data-i18n");
      if (traducciones[idioma][clave]) {
          elemento.textContent = traducciones[idioma][clave];
      }
  });

  // Close the modal after changing language
  const modal = document.getElementById("language-modal");
  if (modal) {
    modal.style.display = "none";
  }

  localStorage.setItem("idioma", idioma);
}

// Modal handling functions
function openLanguageModal() {
  const modal = document.getElementById("language-modal");
  if (modal) {
    modal.style.display = "flex";
  }
}

function closeLanguageModal() {
  const modal = document.getElementById("language-modal");
  if (modal) {
    modal.style.display = "none";
  }
}

// Detectar idioma guardado en localStorage
document.addEventListener("DOMContentLoaded", () => {
  const idiomaGuardado = localStorage.getItem("idioma") || "es";
  cambiarIdioma(idiomaGuardado);

  // Manejar el cambio de idioma en el modal
  document.querySelectorAll(".lang-btn").forEach(btn => {
      btn.addEventListener("click", () => {
          const nuevoIdioma = btn.getAttribute("data-lang");
          cambiarIdioma(nuevoIdioma);
      });
  });
  
  // Add event listener to language selector to open modal
  const languageSelector = document.getElementById("language-selector");
  if (languageSelector) {
    languageSelector.addEventListener("click", function(event) {
      event.preventDefault();
      openLanguageModal();
    });
  }
  
  // Add event listener to close modal button
  const closeModalBtn = document.getElementById("close-modal");
  if (closeModalBtn) {
    closeModalBtn.addEventListener("click", closeLanguageModal);
  }
  
  // Close modal when clicking outside of it
  window.addEventListener("click", function(event) {
    const modal = document.getElementById("language-modal");
    if (event.target === modal) {
      closeLanguageModal();
    }
  });
});
